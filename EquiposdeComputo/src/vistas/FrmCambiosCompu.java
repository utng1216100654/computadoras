    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String noserie;
        String enunciadoSQL;
        char estado;
        noserie = txtNoSerie.getText().toUpperCase().trim();

        if (txtNoSerie.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese el n√∫mero de serie para continuar");
        } else {
            enunciadoSQL = "SELECT * FROM computadora WHERE noserie='" + noserie + "'";
            con.consultar(enunciadoSQL);
            
            if (con.irPrimero()) {
                txtProcesador.setText(con.obtenerCampo("procesador"));
                cmbDiscoDuro.setSelectedItem(con.obtenerCampo("discodurogb"));
                cmbRam.setSelectedItem(con.obtenerCampo("ramgb"));
                if (con.obtenerCampo("estado").equals("U")) {
                    rbtnTipoU.setSelected(true);
                } else if (con.obtenerCampo("estado").equals("N")) {
                    rbtnTipoN.setSelected(true);
                } else if (con.obtenerCampo("estado").equals("D")) {
                    rbtnTipoD.setSelected(true);
                
                
            
                } else {
                    JOptionPane.showMessageDialog(this, "No se pueden dar cambios en "
                            + "el sistema.");
                }
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        String noserie = txtNoSerie.getText().toUpperCase().trim();
        String procesador;
        String discoduro;
        String ram;
        char estado;
        String enunciadoSQL;

        procesador = txtProcesador.getText();
        discoduro = cmbDiscoDuro.getSelectedItem().toString();
        ram = cmbRam.getSelectedItem().toString();
        if (rbtnTipoN.isSelected()) {
            estado = 'N';
        } else if (rbtnTipoU.isSelected()) {
            estado = 'U';
        } else {
            estado = 'D';
        }

        if (txtProcesador.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "ERROR: no haz llenado todos los campos!!");
        } else {
            enunciadoSQL = "UPDATE computadora SET discodurogb=" + discoduro + ",ramgb=" + ram + ",procesador='" + procesador + "',estado='" + estado + "' WHERE noserie='" + noserie + "'";
            con.actualizar(enunciadoSQL);
            JOptionPane.showMessageDialog(this, "Actualizacion exitosa.");
            limpiarFormulario();
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

